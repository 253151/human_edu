package human03;

public class HumanExam05_1 {

	public static void main(String[] args) {
		char grade = 'j';
		// if ~ else if ~ else 구문으로 만들기
		if (grade == 'A') {
			System.out.println ("당신은 VIP회원입니다.");
			}
		else if (grade == 'B') {
			System.out.println ("당신은 일반회원입니다.");
		}
			
		else {
			System.out.println ("당신은 비회원입니다.");
		}
	}

}
